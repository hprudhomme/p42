#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int i;
	int j;

	i = 0;
	while (i <= 98)
	{
		j = i + 1;
		while (j <= 99)
		{
			affect(i);
			ft_putchar(' ');
			affect(j);
		
		if (i < 98)
		{
			ft_putchar(',');
			ft_putchar(' ');
		}
		}	
	}
}
